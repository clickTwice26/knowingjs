//Rounding in JS
let y = 10.58888;
console.log(y.toFixed(2)) // without precision

// Rounding with precision
let z = 10.5888;
console.log(z.toPrecision(3)) // it is also rounding //  return string
console.log(z.toPrecision(5)) // it is also rounding //  return string
console.log(z.toPrecision(10)) // it is also rounding //  return string

